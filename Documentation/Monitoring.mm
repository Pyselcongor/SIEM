      Process Information:
    </h3>
    <ul>
      <li>
        <strong>Process ID</strong>&nbsp;is the process ID specified when the executable started as logged in <a title="Windows Security Log Event ID 4688" class="pagelink" href="http://www.ultimatewindowssecurity.com/wiki/SecurityLogEventID4688.ashx">4688</a>.
      </li>
      <li>
        <strong>Process Name</strong>: identifies the program executable that processed the logon.&nbsp;&nbsp;This is one of the trusted logon processes identified by <a title="Windows Security Log Event ID 4611" class="pagelink" href="http://www.ultimatewindowssecurity.com/wiki/SecurityLogEventID4611.ashx">4611</a>.
      </li>
    </ul>
    <h3 class="separator">
      Network Information:
    </h3>
    <p>
      This section identifies&nbsp;WHERE the user was when he logged on.&nbsp;&nbsp;Of course if logon is initiated from the same computer this information will either be blank or reflect the same local computers.
    </p>
    <ul>
      <li>
        <strong>Workstation Name</strong>: the computer name of the computer where the user is physically present in most cases unless this logon was intitiated by a server application acting on behalf of the user.&nbsp;&nbsp;Workstation may also not be filled in for some Kerberos logons since the Kerberos protocol doesn't really care about the computer account in the case of user logons and therefore lacks any field for carrying workstation name in the ticket request message.
      </li>
      <li>
        <strong>Source Network Address</strong>: the IP address of the computer where the user is physically present in most cases unless this logon was intitiated by a server application acting on behalf of the user.&nbsp;&nbsp;If this logon is initiated locally the IP address will sometimes be 127.0.0.1 instead of the local computer's actual IP address.&nbsp;&nbsp;This field is also blank sometimes because Microsoft says &quot;Not every code path in Windows Server 2003&nbsp;is instrumented for IP address, so it's not always filled out.&quot;
      </li>
      <li>
        <strong>Source Port</strong>: identifies the source TCP port of the logon request which seems useless since with most protocols&nbsp;source ports are random.
      </li>
