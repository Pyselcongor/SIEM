    <h2>
      <b>Details</b>
    </h2>
    <h3 class="separator">
      New Logon:
    </h3>
    <p>
      The user who just logged on is identified by the Account Name and Account Domain.&nbsp;&nbsp;You can determine whether the account is local or domain by comparing the Account Domain to the computer name.&nbsp;&nbsp;If they match, the account is a local account on that system, otherwise a domain account.
    </p>
    <ul>
      <li>
        <strong>Security ID</strong>: the SID of the account
      </li>
      <li>
        <strong>Account Name</strong>: Logon name of the account
      </li>
      <li>
        <strong>Account Domain</strong>: Domain name of the account in either the DNS name (can be upper or lowercase) or pre-Win2k NETBIOS domain name.&nbsp;&nbsp;In the case of special subjects (well known security principals) like SYSTEM, LOCAL SERVICE, NETWORK SERVICE, ANONYMOUS LOGON this field will be &quot;NT AUTHORITY&quot;.&nbsp;&nbsp;It can also be &quot;NT Service&quot; as in the case of virtual accounts for services.&nbsp;&nbsp;See above.&nbsp;&nbsp;Finally, if the account is a local account, this field will be the name of the computer.
      </li>
      <li>
        <strong>Logon ID</strong>: a semi-unique (unique between reboots) number that identifies the logon session just initiated.&nbsp;&nbsp;Any events logged subsequently during this logon session will report the same Logon ID through to the logoff event <a title="Windows Security Log Event ID 4647" class="pagelink" href="http://www.ultimatewindowssecurity.com/wiki/SecurityLogEventID4647.ashx">4647</a>&nbsp;or <a title="Windows Security Log Event ID 4634" class="pagelink" href="http://www.ultimatewindowssecurity.com/wiki/SecurityLogEventID4634.ashx">4634</a>.
      </li>
      <li>
        <strong>Linked Login ID</strong>: (Win2016/10) This is relevant to User Account Control and interactive logons.&nbsp;&nbsp;When an admin logs on interactively to a system with UAC enabled, Windows actually creates 2 logon sessions - one with and one without privilege.&nbsp;&nbsp;This is called a split token and this fields links the 2 sessions to each other.&nbsp;&nbsp;See Elevated Token above.
      </li>
      <li>
        <strong>Network Account Name</strong>:<font color="rgb(0, 0, 0)" face="Tahoma,verdana,arial,helvetica,sans-serif">&nbsp;(Win2016/10)&nbsp;</font>&nbsp;This appears to always be &quot;-&quot;.&nbsp;&nbsp;It seems connected to LogonUser() with&nbsp;LOGON32_LOGON_NEW_CREDENTIALS but I've not been able to produce an example.&nbsp;&nbsp;If you have an event with this field filled in please open a forum posting on this page and let us see it.
      </li>
      <li>
        <strong>Network Account Domain</strong>:<font color="rgb(0, 0, 0)" face="Tahoma,verdana,arial,helvetica,sans-serif">&nbsp;(Win2016/10)&nbsp;</font>&nbsp;see above
      </li>
      <li>
        <strong>Logon GUID</strong>: Supposedly you should be able to correlate logon events on this computer with corresonding authentication events on the domain controller using this GUID.&nbsp;&nbsp;Such as linking <a title="Windows Security Log Event ID 4624" class="pagelink" href="http://www.ultimatewindowssecurity.com/wiki/SecurityLogEventID4624.ashx">4624</a>&nbsp;&nbsp;on the member computer to <a title="Windows Security Log Event ID 4769" class="pagelink" href="http://www.ultimatewindowssecurity.com/wiki/SecurityLogEventID4769.ashx">4769</a>&nbsp;on the DC.&nbsp;&nbsp;But the GUIDs do not match between logon events on member computers and the authentication events on the domain controller.
      </li>
    </ul>
