    <h2>
      Details
    </h2>
    <h3 class="separator">
      Subject:
    </h3>
    <p>
      The user and logon session that deleted the object.
    </p>
    <ul>
      <li>
        Security ID:&nbsp;&nbsp;The SID of the account.
      </li>
      <li>
        Account Name: The account logon name.
      </li>
      <li>
        Account Domain: The domain or - in the case of local accounts - computer name.
      </li>
      <li>
        Logon ID is a semi-unique (unique between reboots) number that identifies the logon session.&nbsp;&nbsp;Logon ID allows you to correlate backwards to the logon event (4624) as well as with other events logged during the same logon session.&nbsp;
      </li>
    </ul>
    <h3 class="separator">
      Object:
    </h3>
    <p>
      This is the object just deleted.
    </p>
    <ul>
      <li>
        Object Server: always &quot;Security&quot;
      </li>
      <li>
        Handle ID: is a semi-unique (unique between reboots) number that identifies all subsequent audited events while the object is open.&nbsp;&nbsp;Handle ID allows you to correlate to other events logged (Open <a href="http://www.ultimatewindowssecurity.com/wiki/SecurityLogEventID4656.ashx" title="Windows Security Log Event ID 4656" class="pagelink">4656</a>, Access <a href="http://www.ultimatewindowssecurity.com/wiki/SecurityLogEventID4663.ashx" title="Windows Security Log Event ID 4663" class="pagelink">4663</a>, Close <a href="http://www.ultimatewindowssecurity.com/wiki/SecurityLogEventID4658.ashx" title="Windows Security Log Event ID 4658" class="pagelink">4658</a>)
      </li>
    </ul>
    <h3 class="separator">
      Process Information:
    </h3>
    <ul>
      <li>
        Process ID: This is specified when the executable started as logged in <a href="http://www.ultimatewindowssecurity.com/wiki/SecurityLogEventID4688.ashx" title="Windows Security Log Event ID 4688" class="pagelink">4688</a>.
      </li>
      <li>
        Process Name: Identifies the program executable that accessed the object.
      </li>
      <li>
        Transaction ID: Unknown.&nbsp;&nbsp;Start a discussion below if you have information on this field!
      </li>
    </ul>
